    public void construirComboBox() {

        int tamano = peli.getSalas().size();
        CBTipoSala.removeAllItems();

        for (int i = 0; i < tamano; i++) {

            Sala sala = peli.getSalas().get(i);
            CBTipoSala.addItem(sala.getNombreSala());
        }

    }

